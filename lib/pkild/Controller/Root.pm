=cut

=head2 default

=cut

sub default : Private {
    my ( $self, $c ) = @_;
    if(!defined $c->session->{'user'}){
        $c->response->body( "Login plz." );
    }else{
        $c->response->body( "the application" );
    }
}

sub login : Global {
    my ( $self, $c ) = @_;
    $c->authenticate({
                       id          => $c->req->param("login"), 
                       password    => $c->req->param("password") 
                      });
    $c->res->body("Welcome " . $c->user->username . "!");
}

